    private String validatePassword(String password) {
        if (password.length() < 8) {
            return "Password minimal 8 karakter.";
        }
        if (!password.matches(".*[_!@#$%^&(),.?\\\":{}|<>]*.")) {
            return "Password harus mengandung minimal satu simbol.";
        }
        return null;
    }
