    private void btnREGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnREGActionPerformed
        if (txtUSN.getText().equals("") || txtPW.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap Isi Semua Data.");
        } else {
            String username, password;
            username = txtUSN.getText();
            password = txtPW.getText();

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("UasPboPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            Datalogin existingUser = em.find(Datalogin.class, username);

            if (existingUser != null) {
                JOptionPane.showMessageDialog(null, "Username sudah terdaftar. Gunakan username lain.");
                txtUSN.requestFocus();
            } else {
                // Validasi password menggunakan metode
                String passwordError = validatePassword(password);
                if (passwordError != null) {
                    JOptionPane.showMessageDialog(null, passwordError);

                } else {
                    Datalogin newUser = new Datalogin();
                    newUser.setUsername(username);
                    newUser.setPassword(password);

                    em.persist(newUser);

                    em.getTransaction().commit();
                    JOptionPane.showMessageDialog(null, "Sign Up berhasil");

                    LoginFrame loginFrame = new LoginFrame();
                    loginFrame.setVisible(true);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnREGActionPerformed

